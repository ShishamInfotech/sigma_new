class DrawDottedHorizontalLine extends CustomPainter {
  late Paint _paint;

  DrawDottedHorizontalLine() {
    _paint = Paint()
      ..color = greyColor
      ..strokeWidth = 1
      ..strokeCap = StrokeCap.round;
  }

  @override
  void paint(Canvas canvas, Size size) {
    for (double i = -285; i < 285; i = i + 15) {
      if (i % 3 == 0)
        canvas.drawLine(Offset(i, 0.0), Offset(i + 10, 0.0), _paint);
    }
  }

  @override
  bool shouldRepaint(CustomPainter oldDelegate) {
    return false;
  }
}

String maskNumber(String number) {
  if (number.length != 10 || int.tryParse(number) == null) {
    throw ArgumentError('Input must be a 10-digit number.');
  }
  return number.substring(0, 2) + 'XXXXX' + number.substring(7);
}

// For amount formatting
String formatNumberWithCommas(String number) {
  // Split the number into integer and decimal parts
  List<String> parts = number.split('.');

  // Format the integer part by adding commas
  String integerPart = parts[0];
  String formattedInteger = integerPart.replaceAllMapped(
    RegExp(r'(\d)(?=(\d{3})+(?!\d))'),
    (Match match) => '${match[1]},',
  );

  // Handle the decimal part
  String decimalPart;
  if (parts.length > 1) {
    // If the decimal part has only one digit, add a zero to make it two digits
    decimalPart = parts[1].padRight(2, '0');
  } else {
    // If no decimal part, add ".00"
    decimalPart = '00';
  }

  return '$formattedInteger.$decimalPart';
}
